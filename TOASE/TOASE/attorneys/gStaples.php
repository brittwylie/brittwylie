		<p>George received his B.A. in Government from North Texas State University and received his Juris Doctor from the University of Texas School of Law in 1965.</p>
        
        <p> He began his practice in 1965 as an assistant city attorney for the City of Lubbock. He practiced in Victoria from 1968-1971, and in Tarrant County since 1971. He served as executive director of the Golden Crescent Council of Governments from 1969-1970. He has served as Director of the Texas City Attorneys Association and a member of the Advisory Board of the Municipal Legal Studies Center of the Southwestern Legal Foundation, now The Center for American and International Law. He advises several economic development corporations (Art. 5190.6, Sec. 4A and 4B) and acts as city attorney for the cities of Springtown, Blue Mound, Annetta South, Balch Springs Type A Economic Development Corporation, and Westworth Village.</p>
        
        <p> He is currently defending several cities in state and federal litigation involving takings and federal civil rights claims. He has an active trial and appellate litigation practice primarily limited to the defense of cities and public officials in civil rights, personnel actions, eminent domain, zoning and other issues.</p>
        
        <p> He is a frequent lecturer on a variety of subjects and accepts engagements involving examinations and investigations of events and legal matters involving public officials and employees, as well as the recommendation of practices and procedures to protect private and public employers. He is of counsel to the Fort Worth firm Taylor, Olson, Adkins, Sralla & Elam LLP which primarily represents cities and other governments and their officials and employees.</p>
 

		
