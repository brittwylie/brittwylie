			<p>Carvan serves as General Counsel to several Mental Health Mental Retardation Community Centers as well as the State entity Texas Counsel of Mental Health/Mental Retardation Community Centers and local Workforce Development Boards. The position of general counsel to public entities necessarily involves addressing a variety of legal issues on behalf of the public entities and their employees. In the area of employment law, he has represented clients before the Equal Employment Opportunity Commission, the Texas Commission on Human Rights, the United States Merit Systems Protection Board, and the State and Federal courts of Texas.</p>


