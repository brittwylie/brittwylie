    <h3>Litigation and Appeals</h3>


<p>Local governments, through their elected and appointed leaders and employees, must often make difficult choices to protect the health, safety and welfare of its citizens. Almost invariably, these decisions are not favored or supported by everyone. Disputes can and often do arise. Their resolution on terms favorable to our clients is a very important part of what we do at Taylor, Olson, Adkins, Sralla and Elam, L.L.P. Although our primary purpose as counselors is to advise our clients in a manner that helps them avoid costly litigation, when suits are brought to resolve disputes, the attorneys at TOASE are prepared to advocate on behalf of our clients' interests. While we zealously represent our clients, we are always mindful of the fact that litigation can be costly and that our clients are often funded by the public. As a result, we believe it is a priority for us to develop cost-effective and efficient strategies to bring cases to a favorable resolution as quickly as possible, including by dismissal or summary disposition.<p>


<p>Our attorneys have years of experience representing local government entities, public employees and public officials in many different types of litigation, including:</p>

	<ul>

	<li>Annexation and boundary disputes</li>
	<li>Disputes between different municipalities and local governments</li>
	<li>Water rights</li>
	<li>Code enforcement</li>
	<li>Civil rights challenges</li>
	<li>Construction law<li>
	<li>Contract disputes</li>
	<li>Elections</li>
	<li>Eminent domain and inverse condemnation</li>
	<li>Employment and civil service disputes</li>
	<li>Fair housing</li>
	<li>Open Meetings Act challenges</li>
	<li>Public Information Act cases</li>
    <li>Substandard buildings and nuisance abatement</li>
	<li>Tort claims</li>
	<li>Zoning, land use and development</li>

	</ul>

<p>As this list shows, our experience includes not only defending clients when they are sued but also bringing and prosecuting cases on their behalf when needed to enforce the law or enforce a client's decisions. We represent clients before all Texas and federal courts from the commencement of suit, through discovery, mediation, pre-trial motions and, if the case is not dismissed, at trial before a judge or jury. Our firm also represents clients in cases on appeal to the higher courts, including the various Texas Courts of Appeals, the Texas Supreme Court, the U.S. Court of Appeals for the Fifth Circuit, and the United States Supreme Court.</p>
    
    
